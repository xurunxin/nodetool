type EmptyLocaleObject = Readonly<Record<string, never>>;

type WidenStringLeaves<T> = T extends string
  ? string
  : keyof T extends never
    ? EmptyLocaleObject
    : {
        readonly [Key in keyof T]: WidenStringLeaves<T[Key]>;
      };

